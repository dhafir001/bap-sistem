  <script>
    // Mobile menu toggle
    const mobileMenuBtn = document.getElementById('mobileMenuBtn');
    const mainNav = document.getElementById('mainNav');
    
    mobileMenuBtn.addEventListener('click', () => {
      mainNav.classList.toggle('active');
      const icon = mobileMenuBtn.querySelector('i');
      if (mainNav.classList.contains('active')) {
        icon.classList.remove('fa-bars');
        icon.classList.add('fa-times');
      } else {
        icon.classList.remove('fa-times');
        icon.classList.add('fa-bars');
      }
    });
    
    // Close mobile menu when clicking outside
    document.addEventListener('click', (e) => {
      if (!e.target.closest('nav') && !e.target.closest('.mobile-menu-btn') && window.innerWidth <= 768) {
        mainNav.classList.remove('active');
        const icon = mobileMenuBtn.querySelector('i');
        icon.classList.remove('fa-times');
        icon.classList.add('fa-bars');
      }
    });
    
    // Key localStorage
    const key = 'bap_requests';
    
    function ambilData(){
      try { 
        return JSON.parse(localStorage.getItem(key) || '[]'); 
      } catch { 
        return []; 
      }
    }

    const input = document.getElementById('inputCari');
    const btn = document.getElementById('btnCari');
    const hasil = document.getElementById('hasil');
    const noResult = document.getElementById('noResult');
    const resultContent = document.getElementById('resultContent');

    btn.addEventListener('click', () => {
      const q = input.value.trim();
      if(!q){ 
        alert("Harap masukkan nomor permohonan atau paspor"); 
        return; 
      }
      
      const list = ambilData();
      const found = list.filter(x =>
        x.nomor_permohonan.toLowerCase() === q.toLowerCase() ||
        x.paspor.toLowerCase() === q.toLowerCase()
      );

      if(found.length === 0){
        hasil.style.display = 'none';
        noResult.style.display = 'block';
        return;
      }

      // Tampilkan hasil
      resultContent.innerHTML = found.map(x => {
        // Format tanggal
        const createdDate = new Date(x.created_at);
        const formattedDate = createdDate.toLocaleDateString('id-ID', {
          day: 'numeric',
          month: 'long',
          year: 'numeric'
        });
        
        // Tentukan ikon status
        let statusIcon = 'fa-clock';
        if (x.status === 'approved') statusIcon = 'fa-check-circle';
        if (x.status === 'rejected') statusIcon = 'fa-times-circle';
        if (x.status === 'rescheduled') statusIcon = 'fa-calendar-alt';
        
        return `
          <div class="result-item">
            <div class="result-label">Nomor Permohonan</div>
            <div class="nomor-permohonan">${x.nomor_permohonan}</div>
          </div>
          
          <div class="result-item">
            <div class="result-label">Status</div>
            <div class="status ${x.status}">
              <i class="fas ${statusIcon}"></i> ${x.status.toUpperCase()}
            </div>
          </div>
          
          <div class="result-item">
            <div class="result-label">Informasi Pemohon</div>
            <div><strong>Nama:</strong> ${x.nama}</div>
            <div><strong>Email:</strong> ${x.email}</div>
            <div><strong>Paspor:</strong> ${x.paspor}</div>
          </div>
          
          <div class="result-item">
            <div class="result-label">Tujuan BAP</div>
            <div>${x.tujuan || '-'}</div>
          </div>
          
          <div class="result-item">
            <div class="result-label">Tanggal Pengajuan</div>
            <div>${formattedDate}</div>
          </div>
          
          ${x.schedule && x.schedule.tanggal ? `
            <div class="jadwal-info">
              <h4><i class="fas fa-calendar-check"></i> Jadwal BAP</h4>
              <div class="jadwal-detail">
                <div class="jadwal-item">
                  <span class="jadwal-label">Tanggal</span>
                  <span class="jadwal-value">${x.schedule.tanggal}</span>
                </div>
                <div class="jadwal-item">
                  <span class="jadwal-label">Waktu</span>
                  <span class="jadwal-value">${x.schedule.jam_mulai || ''} ${x.schedule.jam_selesai ? ' - ' + x.schedule.jam_selesai : ''}</span>
                </div>
                <div class="jadwal-item">
                  <span class="jadwal-label">Lokasi</span>
                  <span class="jadwal-value">${x.schedule.lokasi || '-'}</span>
                </div>
                <div class="jadwal-item">
                  <span class="jadwal-label">Petugas</span>
                  <span class="jadwal-value">${x.schedule.petugas || '-'}</span>
                </div>
              </div>
            </div>
          ` : ''}
          
          ${x.catatan_admin ? `
            <div class="catatan-admin">
              <h4><i class="fas fa-sticky-note"></i> Catatan Admin</h4>
              <p>${x.catatan_admin}</p>
            </div>
          ` : ''}
          
          ${found.length > 1 ? '<hr style="margin: 20px 0; border-color: rgba(255,255,255,0.1)">' : ''}
        `;
      }).join('');
      
      hasil.style.display = 'block';
      noResult.style.display = 'none';
      
      // Scroll ke hasil
      hasil.scrollIntoView({ behavior: 'smooth' });
    });

    // Enter key support
    input.addEventListener('keypress', (e) => {
      if (e.key === 'Enter') {
        btn.click();
      }
    });
  </script>
